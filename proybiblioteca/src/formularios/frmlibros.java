    public void limpiar() {
        txtCodigo.setText(null);
        txttitulo.setText("");
        txtautor.setText(null);
        txteditorial.setText(null);
        txtanioedicion.setText(null);
        txtcopias.setText(null);
         txtMateria.setText(null);
        lstlibro.removeAll();
 
    }

    public void mostrarDatos1() {
        lstlibro.removeAll();

        for (int i = 0; i < objlibreria.getTam(); i++) {

            lstlibro.add("codigo:" + objlibreria.vecLib[i].getCodigo());
            lstlibro.add("Nombre:" + objlibreria.vecLib[i].getTitulo());
            lstlibro.add("Autor:" + objlibreria.vecLib[i].getAutor());
            lstlibro.add("Editorial:" + objlibreria.vecLib[i].getEditorial());
            lstlibro.add("Anioedicion:" + objlibreria.vecLib[i].getAnioedi());
            lstlibro.add("Materia:" + objlibreria.vecLib[i].getMateria());
            lstlibro.add("Numero de copias:" + objlibreria.vecLib[i].getNrocopias());
            lstlibro.add("************************************");

        }

    }



