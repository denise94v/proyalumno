    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String titulo, autor, editorial, materia, anioE;
        int copias;

        copias = Integer.parseInt(txtcopias.getText());
        titulo = txttitulo.getText();
        autor = txtautor.getText();
        editorial = txteditorial.getText();
        materia = txtMateria.getText();
        anioE = txtanioedicion.getText();

        objlibreria.vecLib[pos].setTitulo(titulo);
        objlibreria.vecLib[pos].setAutor(autor);
        objlibreria.vecLib[pos].setEditorial(editorial);
        objlibreria.vecLib[pos].setMateria(materia);
        objlibreria.vecLib[pos].setAnioedi(anioE);
        objlibreria.vecLib[pos].setNrocopias(copias);
        mostrarDatos1();

        JOptionPane.showMessageDialog(this, "La  modificacion se realizo con exito");


    }//GEN-LAST:event_btnActualizarActionPerformed
