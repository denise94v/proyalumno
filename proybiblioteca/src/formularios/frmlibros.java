    private void btninsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertarActionPerformed

        int cod = Integer.parseInt(txtCodigo.getText());
        String tit = txttitulo.getText();
        String autor = txtautor.getText();
        String edi = txteditorial.getText();
        String mat = txtMateria.getText();
        String anio = txtanioedicion.getText();

        int numcopi = Integer.parseInt(txtcopias.getText());
        
        objlibro = new libro(cod, autor, tit, edi, anio, mat, numcopi);
        objlibreria.agregalibro(objlibro);
        limpiar();
        mostrarDatos1();
        JOptionPane.showMessageDialog(this, "Libro ingresado con Exito..!!");
    }//GEN-LAST:event_btninsertarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed

        int conta = 0;
        btnActualizar.setEnabled(false);
        int id = Integer.parseInt(txtCodigo.getText());
        pos=objlibreria.buscarlibro(id);
        if(pos>=0){
            txttitulo.setText(objlibreria.vecLib[pos].getTitulo());
            txtautor.setText(objlibreria.vecLib[pos].getAutor());
            txteditorial.setText(objlibreria.vecLib[pos].getEditorial());
            txtanioedicion.setText(objlibreria.vecLib[pos].getAnioedi());
            txtMateria.setText(objlibreria.vecLib[pos].getMateria());
            txtcopias.setText(String.valueOf(objlibreria.vecLib[pos].getNrocopias()));
            btnActualizar.setEnabled(true);
            btnborrar.setEnabled(true);
        }
        else {
            btninsertar.setEnabled(false);
            btnActualizar.setEnabled(false);
            btnborrar.setEnabled(false);            
            JOptionPane.showMessageDialog(null, "Libro no encontrado ");
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

