    private void btnAgregarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarMateriaActionPerformed
       
        
        int posE = objCur.tamE - 1;
        int nMates =objCur.vecE[posE].nmaterias;
        String codMate = txtCodMat.getText();
        String nomMate = txtMateria.getText();
        double n1 = Double.parseDouble(txtN1.getText());
        double n2 = Double.parseDouble(txtN2.getText());
        double n3 = Double.parseDouble(txtN3.getText());
        Materia mate = new Materia(codMate, nomMate, n1, n2, n3);
        objCur.vecE[posE].agregarMateria(mate);
        limpiarMateria();
        if (objCur.vecE[posE].tamM>= nMates) {
            btnAgregarMateria.setEnabled(false);
        }
         JOptionPane.showMessageDialog(this, "MATERIA AGREGADA IMPRIMA");
         limpiar();
            

 
    }//GEN-LAST:event_btnAgregarMateriaActionPerformed

