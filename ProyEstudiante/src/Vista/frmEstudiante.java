    public void mostrarDatos() {
        lstDatos.removeAll();
        for (int i = 0; i < objCur.getTamE(); i++) {
            lstDatos.add("" + objCur.vecE[i].getCedula());
            lstDatos.add("" + objCur.vecE[i].getNombres() + " " + objCur.vecE[i].getApellidos());
          
       for (int j = 0; j < objCur.vecE[i].tamM; j++) {
            
            lstDatos.add(""+objCur.vecE[i].vecM[j].imprimir());
        }
        lstDatos.add("----------------------------------------------------------");
        }

    }
    
    public void imprimirTodos() {
        lstDatos.removeAll();
        for (int i = 0; i < objCur.tamE; i++) {
            lstDatos.add("CEDULA=" + objCur.vecE[i].getCedula());
            lstDatos.add("NOMBRES=" + objCur.vecE[i].getNombres());
            lstDatos.add( "APELLIDOS="+objCur.vecE[i].getApellidos());                  
            for (int j = 0; j < objCur.vecE[i].tamM; j++) {
                lstDatos.add(objCur.vecE[i].vecM[j].imprimir());
            }
            lstDatos.add("----------------------------------------------------------");
        }
    }

