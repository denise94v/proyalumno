    public static String fechaactual() {
        Date fecha = new Date();
        SimpleDateFormat formatofecha = new SimpleDateFormat("dd/MM/YYYY");
        return formatofecha.format(fecha);

    }

    public void limpiar() {
        txtcedula.setText(null);
        txtnombre.setText("");
        txtdireccion.setText(null);
        txttelefono.setText(null);
    }

    public void mostrarDatos1() {
        lstDatos.removeAll();
        for (int i = 0; i < objBanco.getTam(); i++) {

            lstDatos.add("Nro De Cta:" + objBanco.vecCta[i].getNroCuenta());
            lstDatos.add("Nombre:" + objBanco.vecCta[i].getCliente().getNombre());
            lstDatos.add("Tipo de Cuenta:" + objBanco.vecCta[i].getTipoCta());
            lstDatos.add("Dirección:" + objBanco.vecCta[i].getNroCuenta());
            lstDatos.add("telefono:" + objBanco.vecCta[i].getCliente().getTelefono());
            lstDatos.add("Fecha de apertura:" + objBanco.vecCta[i].getFechaAbre());
            lstDatos.add("Saldo : " + objBanco.vecCta[i].getSaldo());
            lstDatos.add("************************************");
        }
    }

