    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here:
        double cantidad = Double.parseDouble(txtcantidad.getText());
        if (rdbdeposito.isSelected()) {
            objBanco.vecCta[psc].depositar(cantidad);
        }
        if (rdbretiro.isSelected()) {
            if (objBanco.vecCta[psc].getSaldo() > 10) {
                objBanco.vecCta[psc].retirar(cantidad);
            } else {
                JOptionPane.showMessageDialog(this, "SALDO INSUFICIENTE");
            }

        }
        lstDatos.removeAll();
        mostrarDatos1();
        lstcuenta.removeAll();
        mostrarDatos2();
    }//GEN-LAST:event_btnregistrarActionPerformed
    int psc = 0;
    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        lstcuenta.removeAll();
        int conta = 0;
        btnregistrar.setEnabled(false);
        int id = Integer.parseInt(txtbuscar.getText());
        for (int i = 0; i < objBanco.getTam(); i++) {
            if (objBanco.vecCta[i].getNroCuenta() == id) {
                psc = i;
                conta++;
                mostrarDatos2();
                btnregistrar.setEnabled(true);
            }
        }
        if (conta == 0) {
            JOptionPane.showMessageDialog(null, "La Cuenta no existe");

        }
    }//GEN-LAST:event_btnbuscarActionPerformed

