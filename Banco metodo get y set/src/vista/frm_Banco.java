    private void btncrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearActionPerformed
        if (objBanco.getTam() >= nClientes) {
            JOptionPane.showMessageDialog(this, "LIMITE DE CUENTAS EXEDIDO!");
        } else {
            String ced = txtcedula.getText();
            String nom = txtnombre.getText();
            String dir = txtdireccion.getText();
            String tlf = txttelefono.getText();
            objCli = new Cliente(ced, nom, dir, tlf);
            int nroCta = Integer.parseInt(txtcuenta.getText());
            String tipo;
            tipo = cmbcuenta.getSelectedItem().toString();
            objCuenta = new Cuenta(nroCta, objCli, lblfecha.getText(), tipo);
            objBanco.agregaCuenta(objCuenta);
            limpiar();
            mostrarDatos1();
            JOptionPane.showMessageDialog(this, "Cuenta creada con Exito..!!");
        }
    }//GEN-LAST:event_btncrearActionPerformed

