    private void btnEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEstadoActionPerformed
        // TODO add your handling code here:
        int conta=0;
        for (int i = 0; i < objVector.getTam(); i++) {
            if (objVector.vec[i].getProm()>=21) {
                lstDatos1.add("Nombre: "+ objVector.vec[i].getAlumno());
                lstDatos1.add("Materia: "+ objVector.vec[i].getMateria());
                lstDatos1.add("Promedio: "+ objVector.vec[i].getProm());
                lstDatos1.add("APROBADO");
                lstDatos1.add("*******************************************");
                conta ++;
                lblNum.setText("Aprobados ="+conta);
            }else
                if (objVector.vec[i].getProm()>=16) {
                lstDatos1.add("Nombre: "+ objVector.vec[i].getAlumno());
                lstDatos1.add("Materia: "+ objVector.vec[i].getMateria());
                lstDatos1.add("Promedio: "+ objVector.vec[i].getProm());
                lstDatos1.add("SUPLETORIO");
                lstDatos1.add("*******************************************");
                conta ++;
            }else
                    if (objVector.vec[i].getProm()<16) {
                lstDatos1.add("Nombre: "+ objVector.vec[i].getAlumno());
                lstDatos1.add("Materia: "+ objVector.vec[i].getMateria());
                lstDatos1.add("Promedio: "+ objVector.vec[i].getProm());
                lstDatos1.add("REPROBADO");
                lstDatos1.add("*******************************************");
                conta ++;
            }
            
            
        }
    }//GEN-LAST:event_btnEstadoActionPerformed

