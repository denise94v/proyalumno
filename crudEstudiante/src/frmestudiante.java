    private void btninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninActionPerformed
        // TODO add your handling code here:
         
        String ced =txtCedula.getText();
        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        String curso = txtCurso.getText();
        int notas = Integer.parseInt(txtNotas.getText());
        int edad = Integer.parseInt(txtEdad.getText());
        double estatura = Double.parseDouble(txtEstaura.getText());
        char estadoc= estadoC();
         objper=new PersonaEstudiante(ced, nombres, apellidos, curso, notas, edad, estatura, estadoc);
         objVec.agregar(objper);      
                

        limpiar();
        mostrarDatos();
        JOptionPane.showMessageDialog(this, "Estudiante ingresado con exito..!!");
    }//GEN-LAST:event_btninActionPerformed

