    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        String cedBuscar = txtCedula.getText();
                
        pos = objVec.buscarPersona(cedBuscar);
        if (pos >= 0) {
            txtNombres.setText(objVec.vec[pos].getNombres());
            txtApellidos.setText(objVec.vec[pos].getApellidos());
            txtCurso.setText(objVec.vec[pos].getCurso());
            txtNotas.setText(String.valueOf(objVec.vec[pos].getNotas()));
            txtEdad.setText(String.valueOf(objVec.vec[pos].getEdad()));
            txtEstaura.setText(String.valueOf(objVec.vec[pos].getEstatura()));
            

            btnBorrar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Cédula de estudiante NO existe");
            
    }//GEN-LAST:event_btnbuscarActionPerformed
    }
