    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed

String cedula = txtCedula.getText();

        String origen = cboOrigen.getSelectedItem().toString();
        String destino = cboDestino.getSelectedItem().toString();
        String nombres = txtNombres.getText(),
                apellidos = txtApellidos.getText(),
                fecha = lblFecha.getText(),
                asiento = lblAsiento.getText();

        objP= new Pasajero(cedula, nombres, apellidos, fecha, destino, cedula, costo, asiento, origen, destino);
        objAv.agregaPasajero(objP);
        limpiar();
    }//GEN-LAST:event_btnReservarActionPerformed
 private void ObtenerTamanioObjetos(int cantX, int cantY) {
        TamX = TableroX / cantX;
        TamY = TableroY / cantY;
    }

