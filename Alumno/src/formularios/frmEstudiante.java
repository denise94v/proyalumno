    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        try {
            String nombre = (txtnombre.getText()),
                    materia = (txtmateria.getText());
            double nota1 = Double.parseDouble(txtnota1.getText()),
                    nota2 =Double.parseDouble(txtnota2.getText()),
                    nota3 = Double.parseDouble(txtnota3.getText());
            obje= new  Estudiante (nombre, materia, nota1, nota2, nota3);
            objbase.agregarCuenta(obje);
           
            JOptionPane.showMessageDialog(this, "libro agregado");
            limpiar();
            
        } catch (ArrayIndexOutOfBoundsException excepcion) {
            JOptionPane.showMessageDialog(this, "arreglo lleno");
        } catch (NumberFormatException excepcion) {
            JOptionPane.showMessageDialog(this, "Verifique los datos algo esta mal");
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "El arreglo aun no esta creado");
        }
    }//GEN-LAST:event_btnagregarActionPerformed
